def multiple(x):
   return x * x
print(multiple(10))

# using lambda function
multiple = lambda x : x * x
print(multiple(5))

# using function
def product(x,y):
    return x * y 

print(product(10,4))

# using lambda function
product = lambda x,y : print(f"The product is {x * y}")
print(product(5,3))